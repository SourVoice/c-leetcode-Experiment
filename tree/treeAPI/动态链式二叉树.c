#include "tree_api.h"
#include <assert.h>
#include <malloc.h>
#include <stdio.h>

typedef struct TREE_NODE
{
    TREE_TYPE value;
    struct TREE_NODE *left;
    struct TREE_NODE *right;
} TreeNode;
/*构建指向树根节点的*/
static TreeNode *tree;
void insert(TREE_TYPE value)
{
    TreeNode *current;
    TreeNode **link;
    link = &tree;
    while ((current = *link) != NULL)
    {
        /*选择子树*/
        if (value < current->value)
            link = &current->left; //进入左节点
        else
        {
            assert(value != current->value);
            link = &current->right;
        }
    }
    /*使link指向分配结点*/
    current = malloc(sizeof(TreeNode));
    assert(current != NULL);
    current->value = value;
    current->left = NULL;
    current->right = NULL;
    *link = current;
}
TREE_TYPE *
find(TREE_TYPE value)
{
    TreeNode *current;
    current = tree;
    while (current != NULL && current->value != value)
    {
        if (value < current->value)
            current = current->left;
        else
            current = current->right;
    }
    if (current != NULL)
        return &current->value;
    else
        return NULL;
}